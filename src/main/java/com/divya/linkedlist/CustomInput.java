package com.divya.linkedlist;

public class CustomInput {
    public int index;
    public int inputValue;

    public CustomInput(int index, int inputValue) {
        this.index = index;
        this.inputValue = inputValue;
    }

    public int getIndex() {
        return index;
    }

    public int getInputValue() {
        return inputValue;
    }
}
